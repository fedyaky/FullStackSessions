package Session2;

public class Calculations {
    public static void main(String[] args) {
        int num1 = 15;
        int num2 = 8;

        int sum = num1 + num2;
        int difference = num1 - num2;
        int multiplication = num1 * num2;
        double division = num1 / num2;
        int remainder = num1 % num2;

        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
        System.out.println("The difference of " + num1 + " and " + num2 + " is: " + difference);
        System.out.println("The multiplication of " + num1 + " and " + num2 + " is: " + multiplication);
        System.out.println("The division of " + num1 + " and " + num2 + " is: " + division);
        System.out.println("The remainder of " + num1 + " and " + num2 + " is: " + remainder);

    }
}
