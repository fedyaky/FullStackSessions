package Session3;

public class forLoopExample {
    public static void main(String[] args) {
        System.out.println("For loop example");

//        print numbers from 1 to 10
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        System.out.println("=============================");
        for (int i = 15; i >= 7; i--) {
            System.out.println(i);
        }
    }
}
